          <h3>RIPPLE</h3>

          <p>This has been another prominent player in the cryptocurrency industry, perhaps even more so than the rest. In January, the XRP was worth around 0.006380 $, but now it is worth 0.983000 $. If you do the calculations, that gives you more than 3,000% of profits. This cryptocurrency, too, is based on the same blockchain code as the rest, but operates in a completely different way.</p>

          <p>First, the primary form of generation of this cryptocurrency is completely different. To 'make' a new bitcoin, for example, a person has to 'mine' on their computer and receive the virtual currency as a reward. In contrast, Ripple is not mined, but is issued as an incentive to provide the cryptocurrency. Also through business development agreements, those involved can be rewarded with Ripple tokens.</p>

          <p>This difference in philosophy comes from the creators' belief that banks are too essential to ignore. They do not believe in excluding banks to make everything decentralized, but in working with them to make the transfer of money faster, more efficient and cheaper. The Ripple network is actually faster than Bitcoin, Litecoin and other altcoin because it does not require mining.</p>

          <p>What the developers have done here is to appeal to a different market, to corporations and companies, instead of the masses. The result has been the investment of banks interested in technology, and they have increased their market capitalization by more than 8 billion US dollars. In fact, the Ripple has barely fallen below the 5 major cryptocurrencies this year thanks to institutional support.</p>

