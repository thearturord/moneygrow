          <h3>LITECOIN</h3>

          <p>Although Bitcoin was still unknown by the media, Charlie Lee had already seen potential benefits with the use of this technology. Later, in 2011 he created Litecoin, based on blockchain technology. The code for blockchain is open source, and anyone can access it for free, so we can have as many altcoins as we want, basically altcoins are made almost every two days.</p>

          <p>Anyway, now we have Litecoin as number 5 in the list of all the cryptocoins according to Coin Market Cap. Today, Litecoin has a value of approximately $ 3 billion in market capitalization and each of those coins has a value of $ 280 at the time of publication. The main reason to invest in Litecoin is that it has an excellent rate of return. At the beginning of the year, each Litecoin was worth $ 4.35, which makes the rate of return close to 2 000%.</p>

          <p>For investors, returns are the main reason to invest, but cryptocurrency has also continued the potential growth of daily use. The main use of Litecoin is in the transfer of money, as with Bitcoin. Litecoin also looks like Bitcoin as a form of payment for various goods and services, which means that it will continue to be relevant in the coming years. Actually, it is even preferred at times because it is faster than Bitcoin. The generation of blocks for Litecoin transactions is easier, therefore, confirmations are much faster and transactions less expensive compared to Bitcoin, even through trading with Forex brokers.</p>

