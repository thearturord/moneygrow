          <p>The OECD PISA report collected a section in 2012 measuring the financial literacy of young people (15 years old) from 18 countries. 13 of them members of the OECD. The results for Spain have not been flattering, obtaining an average score of 484, compared to the average of the OECD countries of 500, being only ahead of Croatia, Israel, Slovakia, Italy and Colombia.</p>

          <p>On average, at level 3, Spanish students are "more self-sufficient in this competence and can analyze the consequences of their spending decisions and make simple financing plans in family contexts. They are able, for example, to evaluate different conditions of a loan or to interpret and assess if an invoice is correct, applying basic numerical operations and considering the payment of taxes or shipping costs. " 5% of students are below level 1, with a knowledge so basic that they would not understand that sometimes buying large quantities, even if the unit price is cheaper, can be uneconomical if you are not going to consume everything. 13% in level 1 and 26% in level 2 means that students have limited financial skills to face a very complex financial world, even for people with specific training. This could explain, in part, the over-indebtedness of families and companies in the Spanish economy.</p>

          <p>The need for a financial education does not need discussion and there are recommendations from the OECD or the European Commission itself.</p>

          <p>The CIS barometer of June 2014 showed that the problems that most affect the sample are directly related to the economy: 46.7% are affected by unemployment and 27.3% by some economic problem. Only 13.8% say they are well informed. Despite this, 20% say that it is the topic that usually speaks with their friends.</p>

          <p>It seems evident that economic-financial competition must be improved not only in Spain, but in the rest of the world. Alfred Marshall, British economist, defined the object of economics as "the study of humanity in the ordinary occupations of life (...)," which is close enough to say that whatever we do can be considered an economy. Lionel Robbins deepened in what we consider would be his object of study: "the Economy understands the human conduct like relation between ends and means scarce with alternative uses". Therefore, we find economy when we act to satisfy some need and renounce other alternatives, since there are restrictions, not only financial, in many occasions of time.</p>

