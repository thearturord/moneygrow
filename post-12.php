          <h2 class="h2">Why will I do not invest in bitcoin or cryptocurrencies?</h2>  <br><br>

          <div class="logo-wrap">
            <img class="logo-test" src="img/post12.jpg" alt="">
          </div>

          <p>I write this after seeing in the news that "bitcoin loses more than 30% of value in its worst week since 2013". We are more than used to hear that the bitcoin collapses and shoots up. Normally, an investment with this degree of volatility is only assumed by very expert speculators and yet in my environment I know many people who are investing in bitcoin. Globally, alarming things are being heard that should be of concern to regulators.</p>

        <h3>Why do many people invest in bitcoin?</h3>

      <p>
        Bitcoin and its technology brought the promise of having an anonymous exchange medium without intermediaries. Actually, this already exists: pay in cash. The advantage of paying with cryptocurrency would be to make it easier to pay at a distance. However, in the real world, buying something with bitcoin or transferring funds - for example to a relative in another country - is not easy at all. There is no app that makes it easy even though many startups are trying it. And even if it becomes usable, the fees are so high that it would not be worth it. Using it to send money to another country and exchange it for traditional currency using a bitcoin ATM can cost 15% commission between issuance and withdrawal.</p>

        <p>In turn, if we do not use it to buy or send money but to invest, intermediaries charge several types of commissions for operating with bitcoins:</p>

        <ol>
          <li>Transaction commission: each intermediary sets it between 0.2% and 3%.</li>
          <li>Fund deposit commission: if you do it with a card, for example, it is not a minor expense and varies a lot depending on the method of payment.</li>
          <li>Fund withdrawal commission: when converting bitcoin into fiat currency some intermediaries apply commission on their part to which they can add commissions from your bank. It also applies if you change the bitcoins to other cryptocurrencies.</li>
          <li>Currency exchange commission: if you deposit euros in an intermediary that only accepts dollars you will support a change commission.</li>
        </ol>

        <p>These commissions are difficult to calculate and in part they are only accessible once the account is created in the intermediary. Drive carefully.</p>

        <p>The biggest problem is not so much the commissions but the history of bad practices accumulated by the exchange or intermediaries. Here are some of the tricks they use blatantly:</p>

        <ol>
        <li>Wash trades - operations of an operator with himself to create volume and influence the price, as reported in Bitfinex.</li>
        <li>Spoofing - huge buy or sell orders to simulate a moment of optimism or pessimism that is canceled as it affects the price. This practice has been denounced in Coinbase and others.</li>
        <li>Front-running - where an operator is able to filter his order before that of the clients at propitious moments.</li>
        </ol>

        <p>It is not possible to emphasize enough the little rigor of the security measures of these intermediaries when they are not directly engaged in manipulating the market. Without the vigilance of any authority, the main exchange of the moment called Mt Gox sank in 2014 losing all the bitcoins (850,000 bitcoins). The most accepted theory is that they suffered massive robberies of their cryptocurrencies due to not having adequate security measures. In 2016 the same thing happened with the Australian Igot exchange and we could make an endless list of incidents of these companies.</p>

        <p>It is difficult to think that these bad practices will disappear without regulation.</p>

       <h3>Bitcoin can not be a store of value</h3>

        <p>The currencies that we use daily, the euro or the dollar, are a deposit of value. This means that I am confident that if I keep 3,000 euros at home a year later, those euros will have a value very similar to the initial one. If my salary is in euros I know - with little variation - what things I can buy with him in the near future.</p>

        <p>A cryptocurrency that aspires to replace the dollar can not have 30% drops in a week, as it has just happened. It had falls of 93% in 2011, 70% in 2013 or 86% in 2014 (apart from the first years where the roller coaster was continuous). Even the most dysfunctional traditional currencies do not reach these extremes. Imagine what the gentleman from Florida should have thought of who bought 2 pizzas for 10,000 bitcoins in 2010.</p>

        <p>It is not possible to  use a currency for daily transactions if its value is erratic. Without stability, no one will ask for a loan in that currency or use it to make purchases.</p>

