            <h3>Demo account - Forex for beginners - What is Forex</h3>

            <p>Forex, or the forex market is also the market where currencies are traded. In its simplest form, a currency transaction can be, for example, when you convert your local currency to a new one for a future vacation. Around the whole market, about 5.3 million dollars are quoted daily between governments, companies, corporations and speculators.</p>

