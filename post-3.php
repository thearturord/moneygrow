          <h2>Cut losses</h2><br>

          <p>
            Often, investors with little experience are too focused on avoiding transactions with losses. They tend to let losses increase with the "hope" that the market will change in their favor and the losses will turn into profits.
          </p>

          <p>The best investment strategies usually include loss-cutting procedures. When an investor takes bearish positions, often too many emotions arise and it is difficult to cut losses correctly. It is best to decide when the losses will be cut
            before starting any investment. Thus the investor knows in advance the maximum limit that can be lost in said investment.</p>

          <p>Controlling the overall risk of the account is another key factor in controlling risk. In other words, each investor should know, before they start investing, how far they are willing to get the losses in their account before stopping to
            invest and rethink their strategy. If you opened an account with $ 2,000 USD, would you be willing to lose the $ 2,000 USD? $ 1,000 USD? As in individual investments, the essential thing is to set a limit and not exceed it.</p><br>

          <h2>Determine the size of the position</h2>

          <p>Before starting an investment program, you have to analyze how much you could lose in the account over a certain period, by lot (drawdown). For example, suppose you have considered that your maximum loss in an investment is 30 pips. This
            would translate to approximately $ 300 USD for a position size of $ 100,000 USD. Suppose now that a position of $ 100,000 is equivalent to a lot. In five consecutive transactions with losses, it would lose $ 1,500 USD (5 x $ 300), which
            would be a difficult period, although not unexpected, in a long-term investment. For an investment account of a lot of $ 10,000 USD, this would result in a loss of 15%. Therefore, although it is possible to invest 5 lots (or more) with a
            $ 10,000 account, this analysis suggests that the "cut" would be too large (at least 75% of the value of the account would be canceled).</p>

          <p>Every investor must consider how much he can lose, at most, per lot before deciding how much he wants to invest in order for the account to lead to tolerable "cuts".</p><br>

