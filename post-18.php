          <h3>LOCATION OF OPEN POSITIONS OF TRADERS</h3>

          <p>As you have probably already understood, traders and investors (even professionals) often make mistakes when trading in the markets and are motivated by their emotions. Therefore, another completely logical idea would be to use your mistakes for our purposes. This is the principle behind the famous Supremacy trading strategy, which can be found in the Forex strategy section of our Forex blog. You can also see the Alligator indicator, which allows you to track the mood of the traders on the trading platform.</p>

          <p>Some people think that if it goes against the crowd, it will always work, because the crowd is constantly wrong. The crowd really loses money in the long term. But not because the trend is being predicted constantly wrong. Most determine them correctly, since it is not difficult or otherworldly.</p>

          <p>Observing the open positions of retail traders when the price approaches significant levels of support / resistance, trend lines, before important news, all this will allow you to look behind the retail market scene and understand what emotions are hidden in the market. head of the average Forex speculator.</p>

          <p>There are many tools to analyze the open positions of traders. Many great brokers have their own tables.</p>

          <p>Of course, we all hear that over 90% of people lose money in the forex market. But, as we have said, this does not mean at all that, in a given period of time, more than 90% of the traders incorrectly predict the movement of the market. According to a simple study that we gave at the beginning of the article, the problem is different. Human psychology is such that it is more likely that at the beginning it will obtain benefits from its operations by closing them as soon as they enter green and let the negative operations run until the end in which it can not be more. According to statistics, the average profit for traders in the EURUSD pair is 48 points, and the average loss is 83 points. This is a difference of almost 70%, and it is critical.</p>

