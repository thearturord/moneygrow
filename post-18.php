          <p>Differences between theoretical and behavioral finances are best seen as follows: theory is the foundation on which one can build to improve understanding of the subject, and behavioral aspects are a reminder that theory does not always work as expected. Consequently, a good knowledge of both points of view can help you make better decisions.</p>

          <p>The idea that financial markets are efficient is one of the basic principles of modern portfolio theory. This principle, defended in the theory of an efficient market, assumes that, at a given moment, prices fully reflect all the information available about a particular market. Since all market participants know the same information, no one will have an advantage in forecasting profitability, since no one has access to information that is inaccessible to others. In efficient markets, prices become unpredictable, therefore, the patterns are not traceable, which completely denies any planned approach to trading. On the other hand, research on financial behavior that studies the impact of investor psychology on courses reveals some predictable patterns in markets.</p>

          <p>In theory, all the information is distributed equally. In fact, if it were true, then the use of privileged information would not exist, there would never have been an unexpected bankruptcy. There would be no need for the Sarbanes-Oxley Act (Sarbanes-Oxley Act, 2002), which was designed to drive markets towards higher levels of efficiency. And let's not forget that personal preferences and personal skills also play a role. Obviously, there is a gap between theory and reality.</p>

          <p>In theory, everyone makes rational decisions. Of course, if everything were rational, there would be no speculation, bubbles and irrational exuberance. Nobody would buy when the price is high, and would not sell in panic when the price falls. Without taking into account the theory, we all know that there is speculation, that the bubbles grow and explode.</p>

