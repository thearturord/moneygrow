          <p>Despite the fact that financial behavior points to numerous deviations from rationality, they offer few solutions to make money from the passions of the market. Robert Shiller, author of Irrational Exuberance, 2000, revealed that in the late 1990s, the US stock market. It was in the center of the bubble. But he could not say when it would burst. Similarly, in the fall of the market, specialists in human behavior can not tell us when the market will reach the bottom. However, they can describe how this is likely to occur.</p>

          <p>Human behavior specialists have not yet created a sensible model that really predicts the future, rather than simply explaining what the market has done in the past. The main lesson is that the theory does not tell people how to make a profit. Instead, he says that psychology influences the fact that market prices deviate from the norm over time.</p>

