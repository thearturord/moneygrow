          <p>It is very important that you know from the beginning that Forex is a zero-sum game, this means that you earn money, "someone" loses that money and vice versa what makes it something very similar to a poker game (for giving an illustrative example) where everyone can be in spite of their own money so that others can earn it and that is a cause that is found in the market for the merchants (we have all lost money in Forex). It should also be clarified that in fact it is not exactly a zero-sum market since there are commissions / spreads charged by the broker, although it is a negative sum game that is not understood, at least among the participants in the market (traders ) is zero-sum.</p>

