          <p>While it is true, Forex can not be a regulated market because it is decentralized, if there are regulations in different countries that apply to brokers and it is transcendental to consider them. The most important is the regulation of the United Kingdom, the FCA (Financial Council Authority) whose regulation is the strictest of all and therefore the most reliable. In the United States there is the NFA that also enjoys reliability and in other countries there are also regulations but less strict than those previously mentioned. Unfortunately NOT in all countries there is regulation (for example here in Chile does not exist) so I personally prefer to operate with brokers that are regulated by the FCA and remember that you will give your money to your broker and it would be really unfortunate that he fell into bad hands.</p>

          <h3>7.- YOU CAN BE PROVIDED UP TO 1000 TIMES YOUR INVESTMENT TO OPERATE</h3>

          <p>A very typical concept of the Forex market is leverage (leverage in English). Thanks to this, the retail trader can access the market and take positions in currencies for an amount much higher than the amount available in his account since some brokers allow leverage up to 1000 times the amount available in the trader's account. For example, with some brokers it is possible to acquire € 1,000,000 with only $ 1000 USD deposit in your account. This without any interest or additional documentation to the required for the opening of the account but the leverage is a double-edged sword since if the position you take is incorrect (upside bets for example and goes down) plays you in against and if you use a leverage of 400 times, it means that only a 0.25% down is enough for your account to close and you have no funds to operateThe leverage is defined in this way 1: 100, always being «1» at the beginning which indicates your deposit (1 time) and on the right the multiplication or credit that you are offered, being in the case of the «100» example what it means that with your balance in the broker's account, they allow you to operate up to 100 times that amount. Because of the danger that this entails and the need to use leverage on the part of those of us who are private traders (we do not have the capital of "Bank of America" or "Citibank"), it is necessary to use the "stop loss", a tool used to stop losses up to a certain margin strategically established by each trader.</p>

          <p>It should be noted that corporate traders (Banking) do not use stop-loss or leverage. Given the large capital that banks handle, they do not require the use of leverage to make millions of transactions and therefore are not subject to the risk of suffering a significant loss in percentage terms of their capital in case of having a fall in their positions. they can avoid using Stop-loss.</p>

