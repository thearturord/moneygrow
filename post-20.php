          <p>The scams in the Forex market appeared after the decentralization of the industry, and although the regulators fight against the possible deceptions and scams, these, unfortunately, continue to affect the players of the forex market. To avoid falling into one of them, in this article we review the 10 worst scams that have been discovered. We recommend that you read the article carefully to detect possible patterns in order to discover a future scam.</p>

          <h3>KEITH SIMMONS AND DEANNE SALAZAR</h3>

          <p>These two people were responsible for the elaborate Forex scam known as Black Diamond that took place between 2007 and 2010. The couple operated several hedge funds, along with other partners aware of the crime they were committing, which attracted customers to invest in the Forex market. From 2007 to 2010, more than 240 clients had invested a total of 35 million US dollars.</p>

          <p>At the end of the whole, the scammers could not pay their investors because they had a pyramid scheme and they had never invested money. After the CFTC investigations ended in 2011, Simmons was sentenced to 40 years in prison for 40 years while Salazar received a 4.5-year sentence after making a confession.</p>

          <h3>TREVOR COOK</h3>

          <p>Cook founded Universal Brokerage Services, a Forex trading company, in 2003 and began to publicize its services through the radio. He was arrested and imprisoned in 2010 when investors could not recover his money during the 2008 financial crisis. It turned out that Cook had invested client money through Crown Forex SA, but the company had problems with the Swiss regulator. In the end, more than 1,000 investors lost more than $ 190,000,000 in one of the biggest Forex scams.</p>

          <h3>ADRIAN SHIROMA</h3>

          <p>This was a scammer whose scheme was discovered by the Forex Trading Heroes forum last year. A writer investigated how a trader named Adrián Shiroma created a website to promote his Forex investment business. On the website, he claimed to have been successful in the industry since 2009, urging investors to trust him with their money. The same scammer later started promoting his own Forex broker company under the name of MitsuiFX, in which it was also discovered that it was not regulated or that he even had a permanent address.</p>

          <p>We can not know how many people were scammed by Adrian Shiroma, but there is a high probability that he deceived dozens of unsuspecting investors.</p>

          <h3>COLIN DAVIDS</h3>

          <p>This man created Platinum Forex in 2013 outside of South Africa claiming to provide returns of between 48% and 84%. Investors had invested more than 100 million before the South African regulator, the Financial Services Board (FSB), found out that the company did not have a license as a financial services provider. As it turned out, they were running a Ponzi scheme, which is a fraudulent investment operation, and what's worse, they used the money for personal expenses.</p>

          <h3>LUIS ALONSO SERNA</h3>

          <p>This man was a pastor in Los Angeles, California, but on the other hand he executed the Ponzi scheme based on the Forex market. He had marketed the platform to members of his congregation, making them invest more than $ 4 million. The members had taken loans from their homes to get that much-desired 20% monthly return, which even the best trader in the Forex world can not promise.</p>

          <h3>TIMOTHY COUGHLIN</h3>

          <p>He claimed to be a former naval army pilot, a line he used to lure more than 5,000 unsuspecting investors to shed $ 12.8 million. Forex regulators found no record of him being enrolled in the military, but it seems that this was enough to appear reliable to investors. The investment funds, mostly, came from residents of the United States, and others, the minority, came from residents outside the country.</p>

          <h3>DAVID HAWKINS</h3>

          <p>This sheriff also executed a modest Ponzi scheme within his city of Colorado, managing to obtain 1.2 million dollars from investors after promising a 20% monthly profit. After executing the scheme for a short period of time, he discovered that the plan would fall apart and chose to voluntarily reimburse his investors a total of $ 1,000,000. His investors were mainly friends and co-workers, so the remorse he felt was understandable. The courts also gave him a light sentence of 30 months for a crime that could have cost him up to 20 years in prison.</p>

          <h3>GERALD ROGERS</h3>

          <p>After creating the company Premium Income Corp. from the moment the Forex market was decentralized, Rogers promised huge profits through the Forex market. The scam spread through 3 companies he had. Already in 2005, it had raised more than 100 million US dollars. After the complaints filed with the SEC, the company had to return $ 36,000,000 to the investors, however, and fortunately, Rogers is now in prison.</p>

          <h3>ORION INTERNATIONAL INC.</h3>

          <p>This is another of the first fraud schemes in the Forex market, which earned $ 40 million by stealing several investors' money in 2003. When it was discovered, the president of the company was fined $ 150,000,000 and imprisoned.</p>

